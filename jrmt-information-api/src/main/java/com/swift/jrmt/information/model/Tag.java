package com.swift.jrmt.information.model;

import com.swift.jrmt.common.model.BaseModel;


/**
 * 日期  : 2016-07-29
 * 作者  : YJB
 * 项目  : tag
 * 功能  :  标签
 * 
 **/
public class Tag extends BaseModel {

	
	private static final long serialVersionUID = 4106516797866512878L;

	/**创建人Id**/
	private Long userId;

	/**-1删除，0冻结，4正常**/
	private Integer status;

	/**标签名**/
	private String title;

	/**发布时间**/
	private java.util.Date pubTime;


	public void setUserId(Long userId){
		this.userId = userId;
	}

	public Long getUserId(){
		return this.userId;
	}


	public void setStatus(Integer status){
		this.status = status;
	}

	public Integer getStatus(){
		return this.status;
	}

	public void setTitle(String title){
		this.title = title;
	}

	public String getTitle(){
		return this.title;
	}

	public void setPubTime(java.util.Date pubTime){
		this.pubTime = pubTime;
	}

	public java.util.Date getPubTime(){
		return this.pubTime;
	}

}
